    private void chooseForSaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chooseForSaveButtonActionPerformed
        // TODO add your handling code here:
        if (!sapNumberField.getText().equals("") && !nameField.getText().equals("")) {
            institute = String.valueOf(instituteCombo.getSelectedItem());
            sapNumber = sapNumberField.getText();
            name = nameField.getText();
            JFileChooser jFileChooser = new JFileChooser();

            jFileChooser.setCurrentDirectory(new File(System.getProperty("user.home")));

            jFileChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);

            int fileState = jFileChooser.showDialog(null, "Select Folder");

            if (fileState == JFileChooser.APPROVE_OPTION) {
                File selectedFile = jFileChooser.getSelectedFile();
                fileNameAndPath = selectedFile.getAbsolutePath() + "\\" + name + "-Report of " + institute + "  Sap " + sapNumber + " MCQ " + date + ".txt";
                saveFilePathField.setText(fileNameAndPath);
                System.out.println(fileNameAndPath);
            }
        } else {
            JOptionPane.showMessageDialog(null, "You need to complete above fields before choosing the file saving directory", "Warning", 2);
        }
    }//GEN-LAST:event_chooseForSaveButtonActionPerformed

