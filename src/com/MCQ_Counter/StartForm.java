    private void chooseForLoadButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chooseForLoadButtonActionPerformed
        // TODO add your handling code here:
        JFileChooser jFileChooser = new JFileChooser();

        jFileChooser.setCurrentDirectory(new File(System.getProperty("user.home")));

        FileNameExtensionFilter extension = new FileNameExtensionFilter("TEXT FILES", "txt", "text");
        jFileChooser.setFileFilter(extension);

        int fileState = jFileChooser.showOpenDialog(null);

        if (fileState == JFileChooser.APPROVE_OPTION) {
            File selectedFile = jFileChooser.getSelectedFile();
            loadPath = selectedFile.getAbsolutePath();
            loadFilePathField.setText(loadPath);
        }
    }//GEN-LAST:event_chooseForLoadButtonActionPerformed

    private void loadButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loadButtonActionPerformed
        // TODO add your handling code here:
        loadBtnPressed = true;
        if (loadPath.equals("")) {
            JOptionPane.showMessageDialog(null, "You haven't selected a file", "Eror", 2);
        } else {
            //getting data in the loading file
            fileNameAndPath = loadPath;
            try {
                File loadedFile = new File(loadPath);
                Scanner reader = new Scanner(loadedFile);
                if (reader.hasNextLine()) {
                    institute = reader.nextLine();
                    if (reader.hasNextLine()) {
                        sapNumber = reader.nextLine();
                        if (reader.hasNextLine()) {
                            date = reader.nextLine();
                            if (reader.hasNextLine()) {
                                name = reader.nextLine();
                                reader.nextLine();
                                if (institute.equals("INDEEPA, Gampaha") || institute.equals("SIPWIN, Kurunegalla") || institute.equals("SASIP, Nugegoda")) {
                                    if (reader.hasNextLine()) {
                                        for (int i = 0; i < 50; i++) {
                                            qTotal[i][0] = i + 1;
                                            reader.nextInt();
                                            qTotal[i][1] = reader.nextInt();
                                        }
                                        paperCount = reader.nextInt();
                                        reader.close();
                                        loadedFile.delete();
                                        //after getting data dumping the old file
                                    }
                                } else {
                                    JOptionPane.showMessageDialog(null, "Invalid text File.File must be initially created by this app", "Eror", 2);
                                }
                            } else {
                                JOptionPane.showMessageDialog(null, "Invalid text File.File must be initially created by this app", "Eror", 2);
                            }
                        } else {
                            JOptionPane.showMessageDialog(null, "Invalid text File.File must be initially created by this app", "Eror", 2);
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Invalid text File.File must be initially created by this app", "Eror", 2);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Your text file is empty. Try creating new Report", "Eror", 2);
                }
            } catch (FileNotFoundException ex) {
                Logger.getLogger(StartForm.class.getName()).log(Level.SEVERE, null, ex);
            }
            //now creating a new file  with name, sap number, date (and more) acquired from the previously loaded (now dumped) file
            try {
                File newFile = new File(fileNameAndPath);
                if (newFile.createNewFile()) {
                    System.out.println("File created: " + newFile.getName());
                    try {
                        FileWriter Writer = new FileWriter(fileNameAndPath);
                        Writer.write(institute + "\n" + sapNumber + "\n" + date + "\n" + name + "\n\n");
                        Writer.close();
                        System.out.println("Successfully written.");
                        MCQ_Counter form = new MCQ_Counter();
                        form.setVisible(true);
                        form.pack();
                        this.dispose();
                    } catch (IOException e) {
                        System.out.println("An error has occurred.Nothing written");
                        e.printStackTrace();
                    }
                } else {
                    System.out.println("Unable to Load File");
                }
            } catch (IOException e) {
                e.printStackTrace();
            }
        }

    }//GEN-LAST:event_loadButtonActionPerformed

